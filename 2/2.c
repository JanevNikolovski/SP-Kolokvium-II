#include <stdio.h>
#define MAX 80

void wtf() {
    FILE *f = fopen("sifra.txt", "w");
    char c;
    while((c = getchar()) != EOF)
    {
        fputc(c, f);
    }
    fclose(f);
}

int main() {
    wtf();
    FILE *f = fopen("sifra.txt", "r");
    char c;
    while((c = fgetc(f)) != EOF)
    {
        if (c>='A' && c<='Z')
        {
            c+=('a'-'A');
        }
        if (c>='a' && c<='z')
        {
            if (c + 3 > 'z') {
                c = (c + 3) - 'z' + 'a'-1;
            } else {
                c += 3;
            }
        }
            putchar(c);


    }
    fclose(f);
    return 0;
}
